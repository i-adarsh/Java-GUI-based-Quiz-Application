    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
       if (newExam.getTotalQuestions() == (questionStore.getCount())){
            btnDone.setVisible(true);
            disableAll();
            JOptionPane.showMessageDialog(null,"Questions Set Press Done to Continue ...","Successfull",JOptionPane.INFORMATION_MESSAGE);
        }
        if (newExam.getTotalQuestions() > questionStore.getCount()){
            try{
                validateInputs();
                String correctAnswer = options.get(correctOption);
                    if (newExam.getTotalQuestions() > questionStore.getCount()){
                        Question question1 = new Question(newExam.getExamId(),count,newExam.getLanguage(),
                                option1,option2,option3,option4,correctAnswer,question);
                        try {
                            questionStore.addQuestion(question1);
                            lblTitle.setText((newExam.getTotalQuestions() - count) + " QUESTIONS REMAINING");
                            if (newExam.getTotalQuestions() > counter){
                                counter++;
                            }
                            lblquestionNumber.setText("Question Number "+(counter)+" : ");
                            clearAll();
                            count += 1;
                            if (newExam.getTotalQuestions() == (questionStore.getCount())){
                            btnDone.setVisible(true);
                            disableAll();
                            JOptionPane.showMessageDialog(null,"Questions Set Press Done to Save in the DB  ...","Successfull",JOptionPane.INFORMATION_MESSAGE);
                        }

                        }
                        catch (Exception ex){
                            ex.printStackTrace();
                        }
                        
                    }
                
            }catch (Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnNextActionPerformed

