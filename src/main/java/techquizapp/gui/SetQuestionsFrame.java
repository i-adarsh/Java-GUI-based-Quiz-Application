    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
        if(questionStore.getCount() < newExam.getTotalQuestions())
        {
            int x=newExam.getTotalQuestions()-count+1;
            JOptionPane.showMessageDialog(null, "You still have "+x+" Questions Remaining","Fill All Questions!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            if (questionStore.getCount() == newExam.getTotalQuestions()){
                int ans = JOptionPane.showConfirmDialog(null,"Are You Sure, You Want to Store this Test in DataBase ? ","Are You Sure ?",JOptionPane.YES_NO_OPTION);
                if (ans == JOptionPane.YES_OPTION){
                    boolean examAddResult = ExamDAO.addExam(newExam);
                    QuestionsDAO.addQuestions(questionStore);
                    AdminOptionsFrame adminFrame = new AdminOptionsFrame();
                    adminFrame.setVisible(true);
                    this.dispose();
                    if (examAddResult == true){
                        JOptionPane.showMessageDialog(null,"Exam Added Successfully","Successfully Added",JOptionPane.INFORMATION_MESSAGE);
                        count = 1;
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Exam not Added in the DB","Try Again",JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            else {
                JOptionPane.showMessageDialog(null,"Please Add Total Number of Questions ","Inforamtion",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch (SQLException throwables) {
            JOptionPane.showMessageDialog(null, "DB Error", "SQL Error !!!",JOptionPane.ERROR_MESSAGE);
            throwables.printStackTrace();
        }
    }//GEN-LAST:event_btnDoneActionPerformed

